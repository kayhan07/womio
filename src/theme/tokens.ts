const parseHex = (hex: string) => {
  const k = hex.replace("#", "").trim()
  if (!(k.length === 3 || k.length === 6)) return null
  const norm = k.length === 3 ? k.split("").map((c) => c + c).join("") : k
  const n = Number.parseInt(norm, 16)
  if (Number.isNaN(n)) return null
  return {
    r: (n >> 16) & 255,
    g: (n >> 8) & 255,
    b: n & 255,
  }
}

export const tc = (hex: string) => {
  const k = hex.trim().toUpperCase()
  const exact = (themeHex as Record<string, string>)[k]
  if (!exact) return hex

  const rgb = parseHex(k)
  if (!rgb) return exact

  const { r, g, b } = rgb
  const max = Math.max(r, g, b)
  const min = Math.min(r, g, b)
  const sat = max === 0 ? 0 : (max - min) / max
  const lum = (0.2126 * r + 0.7152 * g + 0.0722 * b) / 255
  const pinkish = r > g && r > b && r - b < 120
  const purplish = r > 90 && b > 90 && g < 170

  if (lum > 0.94) return tokens.colors.surface
  if (lum > 0.86) return tokens.colors.surfaceMuted
  if (lum < 0.18) return tokens.colors.textStrong
  if (lum < 0.34) return tokens.colors.textBody
  if (sat < 0.2 && lum < 0.62) return tokens.colors.textMuted
  if (pinkish && sat > 0.35 && lum > 0.45 && lum < 0.85) return tokens.colors.brand
  if (purplish && sat > 0.3 && lum > 0.4) return tokens.colors.accentPurple
  if (sat < 0.18) return tokens.colors.border
  if (sat > 0.55 && r > g && r > b) return tokens.colors.brand
  if (sat > 0.45 && b >= r) return tokens.colors.accentPurple
  if (lum > 0.72) return tokens.colors.border
  if (lum > 0.56) return tokens.colors.textMuted
  if (lum > 0.4) return tokens.colors.textBody
  return tokens.colors.textStrong
}


